<?php 
// Функция для получения всех уникальных годов выпуска фильмов из базы данных
// Принимает объект PDO для подключения к базе
// Возвращает массив годов (целых чисел или строк)
function getAllYears(PDO $pdo): array {
    // Выполняем SQL-запрос:
    // выбираем только уникальные значения поля 'year' из таблицы 'movies'
    // сортируем их по убыванию (от новых к старым)
    $stmt = $pdo->query("SELECT DISTINCT year FROM movies ORDER BY year DESC");

    // Возвращаем массив всех найденных годов
    // PDO::FETCH_COLUMN позволяет получить одномерный массив со значениями столбца 'year'
    return $stmt->fetchAll(PDO::FETCH_COLUMN);
}
?>
