<?php
// Функция для получения всех жанров из базы данных
// Принимает объект PDO для подключения к базе данных
// Возвращает массив ассоциативных массивов с полями 'id' и 'name' жанров
function getAllGenres(PDO $pdo): array {
    // Выполняем SQL-запрос для выбора всех жанров (id и name) из таблицы genres
    $stmt = $pdo->query("SELECT id, name FROM genres");

    // Возвращаем результат в виде массива, где каждый элемент — ассоциативный массив жанра
    return $stmt->fetchAll(PDO::FETCH_ASSOC);
}
?>
