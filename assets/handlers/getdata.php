<?php
// Функция для выборки и обработки данных из массива $_POST по заданным ключам
// Принимает:
// - $keys: массив ключей, которые нужно получить из $_POST
// - $trim: флаг, нужно ли обрезать пробелы у значений (по умолчанию true)
// Возвращает:
// - ассоциативный массив с ключами из $keys и соответствующими значениями из $_POST (или пустой строкой, если ключ не найден)
function getPostData(array $keys, bool $trim = true): array {
    $data = [];

    foreach($keys as $key){
        // Проверяем, есть ли в $_POST значение с данным ключом
        if(isset($_POST[$key])){
            $value = $_POST[$key];
            // Если нужно, обрезаем пробелы по краям, иначе оставляем как есть
            $data[$key] = $trim ? trim($value) : $value;
        } else {
            // Если ключ не найден, записываем пустую строку
            $data[$key] = '';
        }
    }

    return $data;
}
?>
